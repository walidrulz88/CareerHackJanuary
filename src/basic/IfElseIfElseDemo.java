package basic;

public class IfElseIfElseDemo {

	public static void main(String[] args) {
		int num1=80;
		int num2=45;
		if(num1>num2)
			System.out.println("num1 is bigger");
		else if(num2>num1)
			System.out.println("num2 is bigger");
		else if(num1==num2)
			System.out.println("both are the same");
		else
			System.out.println("error");
		
		
			

	}//end main

}//end class
